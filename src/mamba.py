def score_answer(model, tokenizer, prompt, answer, max_length=2048):
    full_text = prompt + answer

    enc_full = tokenizer(
        full_text,
        return_tensors="pt",
        truncation=True,
        max_length=max_length
    ).to("cuda")

    enc_prompt = tokenizer(
        prompt,
        return_tensors="pt",
        truncation=True,
        max_length=max_length
    ).to("cuda")

    input_ids = enc_full["input_ids"]
    prompt_len = enc_prompt["input_ids"].shape[1]

    with torch.no_grad():
        out = model(input_ids)

    logits = out.logits if hasattr(out, "logits") else out

    # shift for causal LM
    shift_logits = logits[:, :-1, :]
    shift_labels = input_ids[:, 1:]

    # 只算 answer 部分
    answer_start = max(prompt_len - 1, 0)
    answer_logits = shift_logits[:, answer_start:, :]
    answer_labels = shift_labels[:, answer_start:]

    loss = F.cross_entropy(
        answer_logits.reshape(-1, answer_logits.size(-1)),
        answer_labels.reshape(-1),
        reduction="mean"
    )

    return -loss.item()


# 4️⃣ 预测答案（yes / no / maybe）
answer_choices = [" yes", " no", " maybe"]

def predict_answer(model, tokenizer, prompt):
    scores = {}
    for ans in answer_choices:
        scores[ans.strip()] = score_answer(model, tokenizer, prompt, ans)

    pred = max(scores, key=scores.get)
    return pred, scores


