"""**Hypothesis 2: The combination of Selective Delta and Selective C will achieve lower perplexity than using only one of them or removing selectivity.**"""

import torch
import torch.nn as nn
import torch.nn.functional as F

class SimpleSelectiveSSM(nn.Module):
    def __init__(self, d_model, d_state=16, selective_delta=True, selective_c=True):
        super().__init__()
        self.d_model = d_model
        self.d_state = d_state
        self.selective_delta = selective_delta
        self.selective_c = selective_c

        self.in_proj = nn.Linear(d_model, d_model)
        self.delta_proj = nn.Linear(d_model, d_model)
        self.c_proj = nn.Linear(d_model, d_model)
        self.fixed_delta = nn.Parameter(torch.zeros(d_model))
        self.fixed_c = nn.Parameter(torch.ones(d_model))
        self.out_proj = nn.Linear(d_model, d_model)

    def forward(self, x):
        batch, seq_len, d_model = x.shape
        u = torch.tanh(self.in_proj(x))
        if self.selective_delta:
            delta = torch.sigmoid(self.delta_proj(x))
        else:
            delta = torch.sigmoid(self.fixed_delta).view(1, 1, d_model)
            delta = delta.expand(batch, seq_len, d_model)

        if self.selective_c:
            c = torch.sigmoid(self.c_proj(x))
        else:
            c = torch.sigmoid(self.fixed_c).view(1, 1, d_model)
            c = c.expand(batch, seq_len, d_model)

        h = torch.zeros(batch, d_model, device=x.device)
        outputs = []

        for t in range(seq_len):
            h = (1 - delta[:, t, :]) * h + delta[:, t, :] * u[:, t, :]
            y = c[:, t, :] * h
            outputs.append(y.unsqueeze(1))

        y = torch.cat(outputs, dim=1)
        return self.out_proj(y)

class SimpleMambaLM(nn.Module):
    def __init__(self, vocab_size, d_model=128, n_layers=4,
                 selective_delta=True, selective_c=True):
        super().__init__()
        self.embed = nn.Embedding(vocab_size, d_model)
        self.layers = nn.ModuleList([
            SimpleSelectiveSSM(
                d_model=d_model,
                selective_delta=selective_delta,
                selective_c=selective_c
            )
            for _ in range(n_layers)
        ])

        self.norm = nn.LayerNorm(d_model)
        self.lm_head = nn.Linear(d_model, vocab_size)

    def forward(self, input_ids, labels=None):
        x = self.embed(input_ids)
        for layer in self.layers:
            x = x + layer(x)
        x = self.norm(x)
        logits = self.lm_head(x)

        loss = None
        if labels is not None:
            shift_logits = logits[:, :-1, :].contiguous()
            shift_labels = labels[:, 1:].contiguous()
            loss = F.cross_entropy(
                shift_logits.view(-1, shift_logits.size(-1)),
                shift_labels.view(-1)
            )

        return {"loss": loss, "logits": logits}

